      <p>Main Forum</p>
      <p>The Main Forum is where all the players can talk or discuss past, present or future events. Important notices are placed in Topics near the top, which also stand out from the normal topics for your benefit.<br>
        Here you can create your own topics, or talk in others.</p>
      <p>Mafia Club</p>
      <p>The Mafia Club is an online chat room, where anyone can discuss whatever they want, freely.<br>
        To refresh the chat room, click the refresh button near the top of the chat room window.</p>
      <p>Crew Forum</p>
      <p>If you are in a crew, the crew forum page is available. It can only be seen by the members of the same crew.<br>
        Different crews have their own Forums.<br>
        In the crew forum, the Crew Boss, RHM or council members can post topics as well as the normal members of the crew. They can discuss an attack or just events that have either happened, or will be happening. <br>
        Each Crew Boss has a different rule to keeping the Crew Forum. Make sure you read their rules!</p>
