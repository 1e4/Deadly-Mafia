      <p>Ways to protect your account<br>
      </p>
      <p>1. Never ever give out your password unless it&rsquo;s someone you can really trust. <br>
        If you encounter a message or a topic stating that (username) requires your password DON&rsquo;T GIVE IT. Admins, Moderators and Help Desk Operators never require the use of your password so if you get a message saying that FenchurchX or MonkeyAvengerV2 require your password don&rsquo;t listen. It&rsquo;s nothing more then a scam to take control of your account.</p>
      <p>2. Never click on a link while you are logged in to your account. If you find a link in the forums saying something like: click this link to receive 100 points&hellip;<br>
        9 times out of 10 it will be a scam. Always log out of your account and any other accounts online (e.g. e-mail or bank accounts) before clicking on any links.</p>
      <p>3. Don&rsquo;t share your account with anyone. Not even trusted friends.<br>
