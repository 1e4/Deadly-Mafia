      <p>In Game Actions</p>
      <p>Bullet Factory</p>
      <p>The Bullet Factory is quite simply &ndash; where you buy bullets. The Bullet Factory owners can choose the price of each bullet.<br>
        Sometimes, owners will &lsquo;close&rsquo; the Factory for a short period of time to stock up on bullets. Don&rsquo;t be alarmed if they do!</p>
      <p>Steal A Car</p>
      <p>Also known as &lsquo;Gta&rsquo; or &lsquo;Grand Theft Auto&rsquo;, Steal A Car is basically what it says on the tin. You steal a car.<br>
        Can&rsquo;t get much simpler then that!<br>
        Once you have stolen a car, it is automatically transferred to your garage. The higher your skill with stealing a car, the better the car you can get away with.</p>
      <p>Drugs</p>
      <p>Here you control your drug operations. Some missions require you to buy drugs from one country and transfer them to another.<br>
        The higher your rank, the more drugs you can carry. The more drugs you transport, the less your chances are of getting caught.<br>
        Below is a list of ranks and the number of drugs that can be carried at one time:</p>
