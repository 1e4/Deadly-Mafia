      <p>General</p>
      <p>Daily Newspaper</p>
      <p>The Daily Newspaper is a page where Admins, Moderators or Writers post all the latest news and details. All the essential information regarding Empire 2010 such as competitions or new features can be found on the Daily Newspaper. Keep an eye out!</p>
      <p>Gangsters Online</p>
      <p>This is where you can view all the players, Admins, Mods and Hdo&rsquo;s that are online. [color=blue]Admins[/color] are displayed in [color=blue]Blue[/color]. [color=red]Mods[/color] are displayed in [color=red]Red[/color] and [color=green]Hdo&rsquo;s[/color] are displayed in[color=green]Green[/color].<br>
        Crew owners are displayed as [b]Bold[/b]<br>
        An RHM of a crew is displayed as [i]Italics[/i]<br>
        Property or Casino owners are displayed as [u]Underlined[/u]<br>
        Your friends are displayed in [color=yellow]Yellow[/color](Once they&rsquo;re added to your friends list)</p>
      <p>The International</p>
      <p>The Head writer and his team of Reporters and Writers write an article once every week. Each article tells a different story about Empire 2010 and includes the latest news and stories that have occurred in Empire 2010. Some articles include interviews with players that achieved an extraordinary feat or the Empire 2010 staff for their views or news on features that will be making their way to the shelves soon.</p>
      <p>Your Crew</p>
      <p>If you own a crew or are a [i]RHM[/i] of a crew, this page allows you to survey general info on your crew. [b]Crew Bosses[/b] and [i]RHM&rsquo;s[/i] also get a special page where they can hand over control to a different member or invite players to join the crew or kick players from the crew.</p>
      <p>If you are a normal member of a crew then this page shows you standard info on your crew. You can also deposit money into the crew bank via the page as well as buying businesses to generate income for the crewmembers.</p>
      <p>Find Gangsters</p>
      <p>Here you can enter a person&rsquo;s name as well as whether they&rsquo;re alive or dead and see the results. If more then 1 player has the name (e.g. Thunder would also have ThunderX and ThunderV2) then all the names will appear. You can also use this page to search for Empire 2010 staffs that aren&rsquo;t online as well as players who aren&rsquo;t online.</p>
      <p>Photo Album</p>
      <p>The Photo Album contains pictures of Empire 2010 staff as well as normal players who have given links to their personal pictures. To give your link, you need to host your picture on a free hosting site (see miscellaneous at the top).</p>
      <p>Airport</p>
      <p>This is where you can travel via a Plane to different countries. You can also buy Planes from The Underground. If you don&rsquo;t own a plane, then you have to wait an hour between travelling. The more expensive your plane is, the better it is! The most expensive plane lets you wait a mere 15 minutes between travelling.</p>
      <p>Kill</p>
      <p>Here you can either search for a player, or if you&rsquo;ve already found one then you can kill him/her. Admins and Moderators cannot be killed.<br>
        To search for a player, you need to enter his/her name in the space below &lsquo;Hunt&rsquo; and then enter a time as well as where you want to search.</p>
      <p>Once you have searched for a player you need to enter the amount of bullets you want to fire. Be warned, the higher the rank and the better protection the target has, will mean the higher the weapon and/or the higher the number of bullets you will need to shoot at the target.</p>
      <p>The Underground</p>
      <p>The Underground is the place where you can buy all the necessities. From guns, to protection to planes &ndash; all the useful items that you&rsquo;ll need in Empire 2010.</p>
      <p>The more expensive the equipment or plane, the better it is. The best weapon will allow you to use fewer bullets against a target.<br>
        The best protection means your attacker will need more bullets to kill you.<br>
        The best planes &ndash; (See Airport)</p>
      <p>Sell</p>
      <p>Here you can sell items that you have. You can collect items by buying them from an auction, or by receiving them from a successful crime instead of money.</p>
      <p>Auctions</p>
      <p>Auctions are a place where players can bit for items. Cars and bullets are the most common items to be sold at an auction. <br>
        Private auctions take place between two people, and are used to give a person item(s) that cannot be given any other way (e.g. bullets).</p>
      <p>Hitlist</p>
      <p>The Hitlist is where players can be placed in a list, along with a price. Once the player on the Hitlist has been killed, the price beside his name will immediately be transferred to your account. Be careful! Powerful friends may protect the target&hellip;</p>
