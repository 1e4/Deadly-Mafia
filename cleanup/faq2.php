      <p>Crime Scene</p>
      <p>Crimes are the standard way of making money. The harder the crime, the more money you receive as well as receiving more rank points.<br>
        You can earn anywhere between &pound;100 - &pound;10,000 via crimes.<br>
        You can only do one crime every 2 minutes.</p>
      <p>Bank</p>
      <p>The bank is where you receive or give money to another player. The owner of the bank chooses the interest rate. The interest rate is given to the owner once the transaction is done.</p>
      <p>e.g. You have &pound;500 to give to another player. The interest rate is 10%. 10% of &pound;500 is &pound;50. The other player only receives &pound;450 while the bank owner receives &pound;50.</p>
      <p>Swiss Bank</p>
      <p>A Swiss bank account will remain throughout Empire 2010 unless the game has been reset.<br>
        Once you make a Swiss bank account, you receive your bank number and personal pin number. You can place as much money as you want in your Swiss account.<br>
        If your account gets banned or killed for any reason, then you can still access your Swiss account and your money once you have made a new account.</p>
      <p>Missions</p>
      <p>Missions can make you rich in a very short time. They are fun and mostly easy to do.<br>
        Missions vary, from drug runs (transporting drugs from different countries) to stealing cars or killing people. Whatever the mission, you&rsquo;ll receive a money and/or bullet reward once the mission is complete. You will also receive a message in your Inbox, telling you of how much money and/or bullets you have received.</p>
      <p>Organised Crime</p>
      <p>Organised Crime or &lsquo;Oc&rsquo; is a team of players who buy equipment to rob a bank. There are four people in every team, no more, no less. A leader, driver, WE (or Weapons Expert) and an EE (or Explosives Expert). Each member of the team has to buy equipment, the more you buy and the more expensive it is, the better the chance of you receiving more money from the Oc. <br>
        If a member doesn&rsquo;t buy enough equipment, or just cheap equipment, then there is a high chance that the Oc will fail and you will be placed in Jail.<br>
        If you fail an Oc, you&rsquo;ll be unbreakable in Jail, meaning you have to wait around 5 minutes or more before you can attempt another crime.</p>
      <p>You can only do an Oc once every 24 hours.</p>
      <p>Extortion</p>
      <p>Extortion is like crimes, except that you steal money or items from other people. You can make a decent amount of money from Extortion, but you&rsquo;ll end up getting more items then money.</p>
      <p>Territory</p>
      <p>Crews or Individuals can own territories. If you own a territory, you can end up making a large amount of money.<br>
        Once your account is banned or killed, your territory is available to other players. They have the option of taking it over.</p>
      <p>Arm Wrestling</p>
      <p>Arm wrestling is a great way of making money although it&rsquo;s very time-consuming. <br>
        Basically, you can train up your Arm Wrestling levels by completing training exercises. For every 100% worth of exercises you complete, you go up a level.<br>
        You also have the option of challenging an opponent or starting a challenge set by an opponent. The prize is cash, and the higher your level is, the better your chances are of winning.</p>
      <p>Crews</p>
      <p>A Crew is a group of people who have come together, either for the same beliefs or because they have a close friendship.<br>
        A crew consists of a Crew Boss, an RHM (or Right Hand Man) and the members.<br>
        The crew boss may decide to appoint a crew counsel or Hitsquad leader, although that&rsquo;s his/her decision.<br>
        The Crew Boss also usually has rules in the crew forum that every member must obey. In times of war, the crewmembers are expected to fight against the attackers, and help their fellow crewmembers out.<br>
        Crews usually only recruit Gangster or higher, although some Crews only recruit close friends. The crew expectations for recruiting members are stated on the crew boss&rsquo;s profile or the crew profile.<br>
      </p>
      <p>Cars</p>
      <p>Garage</p>
      <p>This is where you control your cars. You can ship them to other countries, sell them, repair them, or upgrade them for street racing.</p>
      <p>Street Race</p>
      <p>Street racing can earn you fame, fortune or a lifetime picking food out of dumpsters. You can customise your car in the Garage page. The higher the level for your car parts &ndash; the higher the chance you have of winning the race. You need another player with a car to race.</p>
      <p>Tune Shop</p>
      <p>This is where you can customise any cars you want to race with. Cars do not increase in value on the market if you customise them, however &ndash; you can auction them to other players for a high price depending on how much they have been customised.</p>
      <p>Dealership</p>
      <p>The Dealership owner has to place cars in his Dealership property to be able to sell them. Here you can buy an cars that are in stock. The car prices vary depending on the type and damage.<br>
      </p>
      <p>&nbsp;</p>
      <p>Casinos</p>
      <p>RPS</p>
      <p>More commonly known as &lsquo;Rock, paper scissors&rsquo; &ndash; You can gamble here and either get lucky or get broke. Gambling is a risky way of earning money so be careful!</p>
      <p>Slots</p>
      <p>Slots or &lsquo;Reels&rsquo; are another place to gamble. They are the most difficult casino game to win at, but also the most rewarding.</p>
      <p>Race Track<br>
        Here, you have to choose a car and then bet a certain amount on it. If the car wins, then you get between 2x and 4x your money back. The winning cars are completely random, so don&rsquo;t get caught out!</p>
      <p>Lottery</p>
      <p>Every player on Empire 2010 can buy tickets for the weekly lottery here. The prize is taken from the ticket prices. Once lucky person will get the prize &ndash; (equivalent of all the ticket prices together). Ticket prices are &pound;10,000 each, although the price may change from time-to-time.<br>
        The more tickets you buy, the higher your chance of winning the lottery!</p>
