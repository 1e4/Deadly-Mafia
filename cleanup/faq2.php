      <p>Countries</p>
      <p>This page allows you to see various information on the different Countries. You can view all the businesses owned, as well as the owners. You can also see the Bullet prices and Casino prices for that particular Country.<br>
        If no one owns a particular business, you can travel to the Country where it is and pick it up.</p>
      <p>Attempts</p>
      <p>Here you can view any attempts players have made on your life. If their attempt was successful, then you can&rsquo;t visit this page as your dead! <br>
        If their attempt was unsuccessful, then their name(s) as well as the time they shot you will be listed. Go out and get some revenge!</p>
      <p>Jail</p>
      <p>There are many reasons that you can be put in Jail. For a failed Crime, Gta, Mission or Oc as well as others. <br>
        If you have been put in Jail, then you have to wait a certain time before you are free. Other players who aren&rsquo;t in Jail can bust you out.<br>
        Busting people in Jail is a long and tiring job, but it can earn you fame and rewards from other players who appreciate the favour. The more people you bust out of jail, the easier it becomes for you to bust other people out &ndash; especially higher ranks.</p>
      <p>If you have been put in Jail by a failed Mission attempt or a failed Oc, then your name will have a small symbol next to it. The symbol represents the fact that you are &lsquo;unbreakable&rsquo; from jail, and you have no choice but to sit patiently until the timer runs out.</p>
      <p>Restaurant</p>
      <p>The Restaurant is a necessity in Empire 2010. Here you can buy food that is exchanged as energy. You need energy to increase your chances of a successful Crime, Gta or an Oc. Keep those energy levels filled up!</p>
      <p>The Bar</p>
      <p>The Bar is only open to players who are ranked &lsquo;Gangster&rsquo; or above. The players can buy drinks for other people.<br>
        Beers are the most sought out drink. They allow a random number of rank points to be added to a player&rsquo;s bar, although you cannot buy them for yourself. Someone else has to buy a Beer for you.<br>
        You can only buy 1 drink every 24 hours.</p>
      <p>The Hospital</p>
      <p>The Hospital is available for people who have been injured either by a failed Oc attempt, or by a failure for the attacker to shoot enough bullets into their head.<br>
        The Hospital charges a cheap rate per hour. Every hour spent in the Hospital replenishes 10% of your health.</p>
      <p>Safe House</p>
      <p>Here you have to spend money to be protected by the safe house. The safe house charges money per [u]minute[/u].<br>
        While you are in a Safe House, you cannot be killed.<br>
      </p>
      <p>Communicate</p>
      <p>Inbox</p>
      <p>The Inbox is where all your messages that you have received from various people, are stored. You also have the option of saving a message or deleting it.<br>
        When you have received a message, you will be alerted by a red sentence beside the date, informing you that you have received a new message(s).<br>
        In your Inbox, you can also block certain people from sending you messages. To do this, enter your Inbox, look near the top and access [i]User Information.[/i]</p>
      <p>Saved</p>
      <p>The page where all your saved messages are kept. You have the option of deleting them.</p>
