      <p>This page allows you to send tickets to Help Desk Operators, with any queries or questions that you have about Empire 2010. Hopefully, this FAQ will give you a solid amount of Information, but the Help Desk Operators are always there to help!</p>
      <p>Once you have sent a Help Desk Ticket, please note that the reply to the ticket will appear in the [u]Help Desk Page[/u], not your Inbox.</p>
